                <h1 id="accessibility">Accessibility Evaluation</h1>
                    <p>It is important that all pages coded by CDL comply with Section 508 of the Rehabilitation Act. Section 508 of the Rehabilitation Act requires access to electronic and information technology procured by Federal agencies. The Access Board developed accessibility standards for the various technologies covered by the law. These standards have been folded into the Federal government’s procurement regulations.</p>

                    <p>Open a browser with a site you have made and the readings the readings below. As you read through the concepts and techniques below, evaluate the accessibility of your site.</p>

                    <ol>
                        <li>Complete a couple of the <a href="http://www.webaim.org/simulations/" target="_blank">WebAIM Simulations</a> to experience some of the challenges of someone with these disabilities. This will give you a better understanding of the importance of creating accessible sites. Here are some things to think about:
                            <ul>
                                <li>What were your expectations on how people with disabilities experience the Web?</li>
                                <li>How did they change after this activity?</li>
                                <li>Did you make any discoveries during the activity?</li>
                                <li>What was your biggest challenge?</li>
                                <li>How would you apply this when designing your site?</li>
                            </ul>
                        </li>
                        <li>Read the following articles and begin evaluating the accessibility of your site.
                            <ol>
                                <li><a href="http://www.webaim.org/techniques/userperspective" target="_blank">Considering the User perspective: A Summary of Design Issues</li>
                                <li><a href="http://www.webaim.org/articles/process/evaluate.php" target="_blank">Evaluating Web Site Accessibility</a></li>
                                <li><a href="http://webaim.org/techniques/semanticstructure/" target="_blank">Creating Semantic Structure</a></li>
                                <li><a href="http://webaim.org/techniques/tables/" target="_blank">Creating Accessible Tables</a></li>
                            </ol>
                        </li>
                    </ol>
